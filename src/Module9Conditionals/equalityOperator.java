package Module9Conditionals;

public class equalityOperator {
    public static void main(String[] args) {
        int a = 4;
        int b = 4;
        System.out.println(a == b);
        System.out.println(a = b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);


    }
}
