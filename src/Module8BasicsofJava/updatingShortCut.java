package Module8BasicsofJava;

public class updatingShortCut {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x);
        x +=7;
        System.out.println(x);
        x -=3;
        System.out.println(x);
        x /=12;
        System.out.println(x);
    }
}
